/*
    > (greater than)
    >= (greater than equals)
    < (less than)
    <= (less than equals)
    ==  : Check value only
    === : Check value and data type 
*/

console.log(10 > 20); // false
console.log(20 <= 20); // true 

console.log('20' == 20); // true 
console.log('123' === 123); // false

console.log(1 === parseInt('1')); // true